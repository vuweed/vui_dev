	doorbell_ref_flag = pdTRUE;
#endif	// __DOORBELL_REF__

#if defined ( __BLE_COMBO_REF__ )
	ble_combo_ref_flag = pdTRUE;
#if defined (__APP_SLEEP2_MONITOR__)
	app_sleep2_mon_flag = pdTRUE;
#endif // __APP_SLEEP2_MONITOR__
#endif	// __BLE_COMBO_REF__

#if defined ( __SUPPORT_IPERF__ )
	iperf_support_flag = pdTRUE;
#endif	// __SUPPORT_IPERF__

#if defined ( __SUPPORT_BOOT_FW_VER_CHK__ )
	if (chk_jtag_load())
		version_chk_flag = pdFALSE;
	else
		version_chk_flag = pdTRUE;
#endif	// __SUPPORT_BOOT_FW_VER_CHK__

