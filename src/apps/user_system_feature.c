	atcmd_esc_data_echo_flag = pdTRUE;
#endif	// __DISABLE_ESC_DATA_ECHO__

#if defined ( __ENABLE_ESC_DATA_DUMP__ )
	atcmd_esc_data_dump_flag = pdTRUE;
#endif	// __ENABLE_ESC_DATA_DUMP__

#if defined ( __TRIGGER_DPM_MCU_WAKEUP__ )
	atcmd_mcu_wu_trig_flag = pdTRUE;
#endif	// __SUPPORT_TRIGGER_MCU_WAKEUP__

#if defined ( __SUPPORT_DHCP_SVR__ )
	dhcpd_support_flag = pdTRUE;
#endif	// __SUPPORT_DHCP_SVR__

#if defined ( __SUPPORT_SNTP_CLIENT__ )
	sntp_support_flag = pdTRUE;
#endif	// __SUPPORT_SNTP_CLIENT__

#if defined ( __SUPPORT_NSLOOKUP__ )
	nslookup_support_flag = pdTRUE;
#endif	// __SUPPORT_NSLOOKUP__

#if defined( __SUPPORT_OTA__ ) && defined ( __OTA_HTTP_CLIENT__ )
	ota_support_flag = pdTRUE;
#endif	// __SUPPORT_OTA__ && __OTA_HTTP_CLIENT__

#if defined ( __SUPPORT_BSD_SOCK__ )
	bsd_sock_flag = pdTRUE;
#endif	// __SUPPORT_BSD_SOCK__

#if defined ( __SUPPORT_IPV6__ )
	ipv6_support_flag = pdTRUE;
#endif	// __SUPPORT_IPV6__

#if defined ( __SUPPORT_MQTT__ )
	mqtt_support_flag = pdTRUE;
#endif	// __SUPPORT_MQTT__

#if defined ( __MQTT_ONE_PORT__ )
	mqtt_one_sess_flag	= pdTRUE;
#endif	// __MQTT_ONE_PORT__

#if defined ( __SUPPORT_FACTORY_RESET_BTN__ )
	factory_rst_btn_flag = pdTRUE;
#endif	// __SUPPORT_FACTORY_RESET_BTN__

#if defined ( __SUPPORT_WPS_BTN__ )
	wps_btn_flag_flag = pdTRUE;
#endif	// __SUPPORT_WPS_BTN__

#if defined ( __SUPPORT_CONSOLE_PWD__ )
	passwd_chk_flag = pdTRUE;
#endif	// __SUPPORT_CONSOLE_PWD__

#if defined ( __RUNTIME_CALCULATION__ )
	runtime_cal_flag = pdTRUE;
#endif	// __RUNTIME_CALCULATION__

#if defined ( __DPM_TIM_WAKEUP_TIME_MSEC__ )
	dpm_wu_tm_msec_flag	= pdTRUE;
#endif	// __DPM_TIM_WAKEUP_TIME_MSEC__

#if defined ( __SUPPORT_DPM_DBG_CMD__ )
	dpm_dbg_cmd_flag = pdTRUE;
#endif	// __SUPPORT_DPM_DBG_CMD__

